def prime_numbers_in_list(list_of_numbers):
    return list(filter(lambda x: is_prime(x) == True, list_of_numbers))


def is_prime(number):
    if number < 2:
        return False
    for i in range(2, number // 2 + 1):
        if number % i == 0:
            return False

    return True


